// ModelArchitectureConfig defines the structural parameters of the PNLM transformer.
type ModelArchitectureConfig struct {
	AttentionHeads int
	HiddenLayers   int
	EmbeddingDim   int
	VocabSize      int
}

// PretrainingConfig defines parameters used during the self-supervised pretraining phase
// to learn general RNA biology and molecular grammar.
type PretrainingConfig struct {
	MaskingRatio float64
	LearningRate float64
	BatchSize    int
	Epochs       int
}

// FineTuningConfig defines parameters for task-specific adaptation
// (e.g., disease modeling, interaction prediction, classification).
type FineTuningConfig struct {
	TaskName     string
	LearningRate float64
	BatchSize    int
	Epochs       int
}

// RNAInteractionResult represents the predicted binding affinity and sites between two RNAs.
type RNAInteractionResult struct {
	InteractionProbability float64
	BindingSites           []string // e.g., index ranges or specific motif regions
}

// RNAClassificationResult represents the classification probabilities for RNA types.
type RNAClassificationResult struct {
	IsMRNA   float64
	IsLncRNA float64
}

